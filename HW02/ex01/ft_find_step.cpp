#include "stepper.hpp"

int	ft_find_step(int *arr, int arr_size)
{
	int	i;

	i = 0;
	while (i < arr_size - 1)
	{
		if ((arr[i] - arr[i + 1]) % 2 == 0)
			return (i);
		i++;
	}
	return (-1);
}
